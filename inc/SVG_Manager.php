	public static function init() {
		add_action( 'admin_footer', array( __CLASS__, 'include_all_svg' ) );
	}

	/**
	 * Echo all the svg defs.
	 *
	 * @return void
	 */
	public static function include_all_svg() {
		$svg = self::get_all_vector_defs();
		echo $svg; // phpcs:ignore -- No XSS;
	}

	/**
	 * Get a HTML string that will define all vectors.
	 *
	 * @return string
	 */
	public static function get_all_vector_defs() {
		$output = '';

		foreach ( self::get_date_icons() as $svg ) {
			$output .= $svg['svg'];
		}

		foreach ( self::get_views_icons() as $svg ) {
			$output .= $svg['svg'];
		}

		return $output;
	}

	/**
	 * Get a registered icon details.
	 *
	 * @return array
	 */
	public static function get_icon( $name ) {
		$user_icons = self::get_user_icons();
		if ( isset( $user_icons[ $name ] ) ) {

			return $user_icons[ $name ];
		}

		$category_icons = self::get_category_icons();
		if ( isset( $category_icons[ $name ] ) ) {
			return $category_icons[ $name ];
		}

		$date_icons = self::get_date_icons();
		if ( isset( $date_icons[ $name ] ) ) {
			return $date_icons[ $name ];
		}

		$comment_icons = self::get_comment_icons();
		if ( isset( $comment_icons[ $name ] ) ) {
			return $comment_icons[ $name ];
		}

		$views_icons = self::get_views_icons();
		if ( isset( $views_icons[ $name ] ) ) {
			return $views_icons[ $name ];
		}

		$rating_icons = self::get_rating_icons();
		if ( isset( $rating_icons[ $name ] ) ) {
			return $rating_icons[ $name ];
		}

		return false;
	}

	/**
	 * Get the HTML definition of a svg item.
	 *
	 * @param string $name
	 * @return string
	 */
	public static function get_svg_def( $name ) {
		$icon = self::get_icon( $name );

		if ( is_array( $icon ) ) {
			return $icon['svg'];
		}

		return '';
	}

